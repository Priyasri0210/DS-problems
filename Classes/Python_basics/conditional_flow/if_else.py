a = input("Enter value 1 - ")
b = input("Enter value 2 - ")

a = int(a)
b = int(b)

operation = input("Enter operation - ")

#syntax
#if <condition>:
    #logic for if becomes true
#else:
    #Default code when if becomes false

if operation == '+':
    c = a + b
    print("The addition value is " + str(c))
elif operation == '-':
    c = a - b
    print("The Sub value is " + str(c))
elif operation == '*':
    c = a * b
    print("The Mul value is " + str(c))
elif operation == '/':
    c = a / b
    print("The Div value is " + str(c))
else:
    print('Not supported')

print('Done')

